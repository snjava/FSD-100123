public class Array2DDemo_1
{
	public static void main(String args[])
	{
		double percent[][] = {{66.54, 76.54, 54.14}, {98.14, 89.51, 55.44}};
		System.out.println(percent.length); // return the total number of rows
		System.out.println(percent[1].length); // return the column count of given row
	}
}